package task_2;

public class Main {
    public static void main(String[] args) {
        System.out.println("For loop:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("\nWhile loop:");
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}